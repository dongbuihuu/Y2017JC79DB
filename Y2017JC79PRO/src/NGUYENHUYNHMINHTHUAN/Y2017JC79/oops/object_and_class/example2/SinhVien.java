package Teacher.Y2017JC79.oops.object_and_class.example2;

public class SinhVien {
	// Data fields
	private String sTenSinhVien;
	private float dDiem;
	private byte bTuoi;

	public String getsTenSinhVien() {
		return sTenSinhVien;
	}

	public void setsTenSinhVien(String sTenSinhVien) {
		this.sTenSinhVien = sTenSinhVien;
	}

	public float getdDiem() {
		return dDiem;
	}

	public void setdDiem(float dDiem) {
		this.dDiem = dDiem;
	}

	public byte getbTuoi() {
		return bTuoi;
	}

	public void setbTuoi(byte bTuoi) {
		this.bTuoi = bTuoi;
	}

}
