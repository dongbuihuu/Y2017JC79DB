package JAVACORE_TRAINING.TEACHER.JAVA_BASIC.STRUCTURE_PROGRAMMING.EXAMPLES;

public class IFELSE_DEMO {
    public static void main(String[] args) {
        // 1. Khai báo biến
        int i = 10;

        // 2. Sử dụng phát biểu IF để kiểm tra giá trị của biến i
        if (i <= 0)
            System.out.println("Giá trị của i <= 0");
        else
            System.out.println("Giá trị của i > 0");
    }
}

