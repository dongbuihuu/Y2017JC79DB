package JAVACORE_TRAINING.ADVANCED_TOPICS.JSON_MODULES.JSONEXCERCISE.WITH_JSON_SAMPLE;

public class ImageDescription {
    private String AppId;
    private String Des_Images_Id;
    private int Height;
    private int Width;
    private String Images_URL;
    private String Links;
    private String Status;
    
    /**
     * @return the appId
     */
    public String getAppId() {
        return AppId;
    }
    /**
     * @param appId the appId to set
     */
    public void setAppId(String appId) {
        AppId = appId;
    }
    /**
     * @return the des_Images_Id
     */
    public String getDes_Images_Id() {
        return Des_Images_Id;
    }
    /**
     * @param des_Images_Id the des_Images_Id to set
     */
    public void setDes_Images_Id(String des_Images_Id) {
        Des_Images_Id = des_Images_Id;
    }
    /**
     * @return the height
     */
    public int getHeight() {
        return Height;
    }
    /**
     * @param height the height to set
     */
    public void setHeight(int height) {
        Height = height;
    }
    /**
     * @return the width
     */
    public int getWidth() {
        return Width;
    }
    /**
     * @param width the width to set
     */
    public void setWidth(int width) {
        Width = width;
    }
    /**
     * @return the images_URL
     */
    public String getImages_URL() {
        return Images_URL;
    }
    /**
     * @param images_URL the images_URL to set
     */
    public void setImages_URL(String images_URL) {
        Images_URL = images_URL;
    }
    /**
     * @return the links
     */
    public String getLinks() {
        return Links;
    }
    /**
     * @param links the links to set
     */
    public void setLinks(String links) {
        Links = links;
    }
    /**
     * @return the status
     */
    public String getStatus() {
        return Status;
    }
    /**
     * @param status the status to set
     */
    public void setStatus(String status) {
        Status = status;
    }
}
